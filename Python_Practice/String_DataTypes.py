# Strings
a = "hai, how r u  "
print(a)

b = 'Hey, you '
print(b)

# Multi Line String
c = """ This is Pune city
under Maharastra """
print(c)

# Index Number
print(a[3])
print(b[1])

# Repetition
print(a*2)
print(b*3)

# Slicing
print(a[0:3])
print(a[0:7])
print(a[0:])
print(a[:10])
print(a[-5:-1])

# Step Slicing
print(a[0:9:2])
print(a[10::-1])
print(a[::-1])

# Strips Spaces : It removing  white Spaces
print(a.strip())
print(a.lstrip())
print(a.rstrip())

# Upper,Lower,Title Words
print(a.upper())
print(a.lower())
print(a.title())

# Find,Count,Replace
print(a.count("h"))
print("Replace word")
print(a.find("r",0,3))
print(b.replace("hey","hi"))


